    private void signupBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signupBtnActionPerformed
        try {
            String role = roleCbo.getSelectedItem().toString();
            
            if(!admin.isInputValid(usernameTxt, passwordTxt, roleCbo)){
                JOptionPane.showMessageDialog(this, "Please fill in all the fields.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if(!admin.checkPasswordMatch(passwordTxt, verifyPasswordTxt)) {
                JOptionPane.showMessageDialog(this, "Password and verify password do not match.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if(admin.checkUsername(usernameTxt)){
                JOptionPane.showMessageDialog(this, "The username already exists.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            switch (role) {
                case "Student":
                    system.newPanel(body, StudentRegister);                    
                    break;
                case "Lecturer":
                    system.newPanel(body, LecturerRegister);
                    break;
                default:
                JOptionPane.showMessageDialog(this, "Please select a role!");
            }
        }
        catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_signupBtnActionPerformed

