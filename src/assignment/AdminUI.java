    public void addNametoComboBox(String role, JComboBox<String> nameCbo) throws IOException {
        BufferedReader br = new BufferedReader(new FileReader("data.txt"));
        try {
            String data;
            while ((data = br.readLine()) != null) {
                String arrangedData[] = data.split(",");
                if (arrangedData.length >= 3 && role.equals(arrangedData[2])) {
                    nameCbo.addItem(arrangedData[3]);
                }
            }
        } 
        finally {
            br.close();
        }
    }

    private void detailRoleCboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_detailRoleCboActionPerformed
        int selectedItemIndex = detailRoleCbo.getSelectedIndex();
       // String[] studentCourse = {"Select a course~", "Business", "Finance", "Computer Science",
         //   "Math", "Philosophy", "Psychology", "Science"};
        String[] lecturerDegree = {"Select a degree~", "Middle School", "High School", "Diploma",
            "Bachelor", "Master", "PhD"};
        
        if (selectedItemIndex > 0) {
            String selectedRole = detailRoleCbo.getSelectedItem() != null ? detailRoleCbo.getSelectedItem().toString() : null;
            if (selectedRole != null) {
                admin.clearAllTextFields(EditDetail);
                degreeOrCourseCbo.removeAllItems();

                if ("Lecturer".equals(selectedRole) || "Project Manager".equals(selectedRole)) {
                    previousLbl.setVisible(true);
                    detailPreviousTxt.setVisible(true);
                    studyLbl.setVisible(true);
                    detailStudyTxt.setVisible(true);
                    degreeOrCourseLbl.setText("Highest Degree Obtained");
                    institutionOrEmergencyLbl.setText("Institution Name");
                    try {
                        detailIdCbo.removeAllItems();
                        for (String course : lecturerDegree) {
                            degreeOrCourseCbo.addItem(course);
                        }
                        if ("Lecturer".equals(selectedRole)){
                            admin.addTeacherIdtoComboBox("Lecturer", detailIdCbo);
                        }
                        else if ("Project Manager".equals(selectedRole)){
                            admin.addTeacherIdtoComboBox("Project Manager", detailIdCbo);
                        }
                        
                    } catch (IOException e) {
                    }
                } else if ("Student".equals(selectedRole)) {
                    previousLbl.setVisible(false);
                    detailPreviousTxt.setVisible(false);
                    studyLbl.setVisible(false);
                    detailStudyTxt.setVisible(false);
                    degreeOrCourseLbl.setText("Course");
                    institutionOrEmergencyLbl.setText("Emergency Contact");
                    try {
                        detailIdCbo.removeAllItems();
                        admin.getCourse(degreeOrCourseCbo);
                        admin.addStudentIdtoComboBox("Student", detailIdCbo);
                    } catch (IOException e) {
                    }
                }
            }
        }
        else{
            detailIdCbo.removeAllItems();
        }
    }//GEN-LAST:event_detailRoleCboActionPerformed

